void pwm_ir_on() {
    TCCR2A |= _BV(COM2B1); // Enable PWM output
}

void pwm_ir_off() {
    // disable pwm
    TCCR2A &= ~_BV(COM2B1);

    // set pin to low
    PORT_IR &= ~(_BV(PIN_IR));
}

void ir_setup() {
    DDRD |= _BV(DDD3);

    // 38KHz PWM at 50%
    TCCR2A = _BV(WGM20);
    TCCR2B = _BV(WGM22) | _BV(CS20);
    OCR2A = 210;
    OCR2B = 105;

    // make sure its off
    pwm_ir_off();
}

// NOTE i do not know if its critical to avoid digitalWrite but its it takes
// few microseconds so just to be safe, im using raw AVR
void send_ir(int16_t* mem, uint16_t size) {
    int16_t duration = 0;
    for (uint16_t i = 0; i < size; i++) {
        duration = (int16_t)pgm_read_word(mem + i);
        if (duration > 0) {
            pwm_ir_on();
        } else if (duration < 0) {
            pwm_ir_off();
        }

        // PORT_IR &= ~(1 << PIN_IR) | ((duration > 0 ? 1 : 0) << PIN_IR);
        delayMicroseconds(abs(duration));
    }

    pwm_ir_off();

    // make sure led is off!
    PORT_IR &= ~(1 << PIN_IR);
}

inline void ac_on() {
    send_ir((int16_t*)IR_ON, IR_ON_LEN);
}

inline void ac_off() {
    send_ir((int16_t*)IR_OFF, IR_OFF_LEN);
}

